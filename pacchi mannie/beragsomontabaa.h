void HitBoxMuri(int &ics,int &ipsilon)
{
    for(int i=0;i<24;i++)
    {
        for(int j=0;j<90;j++)
        {
            if(ics==collisioni[i][j]&&ipsilon-9==i)
            {
                switch(dir)
                {
                    case UP:
                        ipsilon++;
                        break;
                    case DOWN:
                        ipsilon--;
                        break;
                    case LEFT:
                        ics++;
                        break;
                    case RIGHT:
                        ics--;
                        break;
                    default:
                        break;
                }
                dir = STOP;
            }
            else if(ics<collisioni[i][j])
            {
                break;
            }
        }
    }
}

